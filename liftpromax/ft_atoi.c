#include "libft.h"

int	ft_atoi(const char *str)
{
	int long	r;
	int			s;

	r = 0;
	s = 1;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		s = 1 - 2 * (*str == '-');
		str++;
	}
	while (*str && *str >= '0' && *str <= '9')
	{
		if (r > 922337203685477580 
			|| (r == 922337203685477580 && *str - '0' > 7))
		{
			if (s == 1)
				return (-1);
			return (0);
		}
		r = r * 10 + (*str - 48);
		str++;
	}
	return (r * s);
}
